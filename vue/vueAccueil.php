  <h2 class="h2centre"> Présentation de la Maison des Ligues de Lorraine </h2>
</br>
<p class="textCentrer">
La Maison des Ligues de Lorraine (M2L) a pour mission de fournir des espaces et 
des services aux différentes ligues sportives régionales et à d’autres structures hébergées. 
La M2L est une structure financée par le Conseil Régional de Lorraine dont l'administration est 
déléguée au Comité Régional Olympique et Sportif de Lorraine (CROSL).
</p>
